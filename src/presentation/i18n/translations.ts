export const translate = (language: AppLanguage, key: string): string =>
  translations[language][key] ?? translations[AppLanguage.PT_PT][key] ?? key;

export const interpolate = (template: string, values: Record<string, string | number>): string =>
  Object.entries(values).reduce(
    (acc, [key, value]) => acc.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value)),
    template,
  );
